#arithmetic operators
num1=45
num2=72
print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1%num2)

#comparison operator
x=6
y=10
z=45.78
print(x==y) #equals
print(x==y and x==z) #equals
print(x>y) #greater
print(x>y and x>z) #greater
print(x>=y) #greater or equals to
print(x>=y and x>=z) #greater or equals to
print(x<=y) #less or equals to
print(x<=y and x<=z) #less or equals to
print(x != y) #not equals to
print(x != y and x != z)  #not equals to

#logical operators
print(x>y and x==6)
print(x>y or x==6)

